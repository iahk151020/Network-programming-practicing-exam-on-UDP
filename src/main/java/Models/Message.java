package Models;

import java.io.Serializable;

/**
 *
 * @author truon
 */
public class Message implements Serializable {
    private int type;
    private Object obj;

    public Message(int type, Object obj) {
        this.type = type;
        this.obj = obj;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public Object getObj() {
        return obj;
    }

    public void setObj(Object obj) {
        this.obj = obj;
    }
    
    
}
